int		ft_iterative_factorial(int nb)
{
	int	i;

	if (nb < 0 || nb > 12)
	{
		write(1, &nb, 1);
		return (0);
	}
	i = nb;
	while (i > 2)
	{
		nb = nb * (i - 1);
		i--;
		write(1, &nb, 1);
	}
	return (nb);
}
