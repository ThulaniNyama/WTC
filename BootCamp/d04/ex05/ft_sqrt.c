int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	while (i * i != nb)
	{
		if (i / 2 > nb)
		{
			break ;
		}
		i++;
	}
	if (i * i == nb)
	{
		return (i);
	}
}
