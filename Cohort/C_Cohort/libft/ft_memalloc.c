#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void *ptr;

	ptr = malloc(size);
	if (ptr && size)
	{
		ft_bzero((void*)ptr, size);
		return (ptr);
	}
	return (NULL);
}
